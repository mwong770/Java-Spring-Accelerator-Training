/**
 * This app does simple calculations.
 * Returns the result of addition, subtraction, multiplication and division of 2 integers.
 * @params args
 *
 */

package com.company;

public class Calculator {

    public int add(int x, int y) {
        return x + y;
    }

    public int subtract(int x, int y) {
        return x-y;
    }

    public int multiply(int x, int y) {
        return x*y;
    }

    public int divide(int x, int y) {
        return x/y;
    }

}
